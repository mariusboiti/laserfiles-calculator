type LanguageContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

function isLocale(value: string): value is Locale {
  return value === 'en' || value === 'ro' || value === 'es' || value === 'fr' || value === 'de';
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('en');

  useEffect(() => {
    try {
      const raw = window.localStorage.getItem(storageKey);
      if (raw && isLocale(raw)) {
        setLocaleState(raw);
      }
    } catch {
      // ignore
    }
  }, []);

  useEffect(() => {
    try {
      document.documentElement.lang = locale;
    } catch {
      // ignore
    }
  }, [locale]);

  const setLocale = (next: Locale) => {
    setLocaleState(next);
    try {
      window.localStorage.setItem(storageKey, next);
    } catch {
      // ignore
    }
  };

  const t = useMemo(() => {
    return (key: string) => {
      const table = translations[locale] ?? translations.en;
      return table[key] ?? translations.en[key] ?? key;
    };
  }, [locale]);

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return ctx;
}

export function useT() {
  return useLanguage().t;
}

export function LanguageSwitcher() {
  const { locale, setLocale } = useLanguage();
  const [open, setOpen] = useState(false);
  const wrapperRef = useRef<HTMLDivElement | null>(null);

  const current = useMemo(() => {
    return localeOptions.find((o) => o.locale === locale) ?? localeOptions[0];
  }, [locale]);

  useEffect(() => {
    if (!open) return;

    function onMouseDown(ev: MouseEvent) {
      const node = wrapperRef.current;
      if (!node) return;
      if (ev.target instanceof Node && node.contains(ev.target)) return;
      setOpen(false);
    }

    document.addEventListener('mousedown', onMouseDown);
    return () => {
      document.removeEventListener('mousedown', onMouseDown);
    };
  }, [open]);

  return (
    <div ref={wrapperRef} className="relative">
      <button
        type="button"
        onClick={() => setOpen((v) => !v)}
        className="rounded-md border border-slate-700 px-2 py-1 text-xs text-slate-200 hover:bg-slate-800"
        aria-haspopup="menu"
        aria-expanded={open}
        title={current.label}
      >
        <span className="text-base leading-none">{current.flag}</span>
      </button>

      {open && (
        <div
          role="menu"
          className="absolute right-0 mt-2 w-44 overflow-hidden rounded-xl border border-slate-700 bg-slate-950 shadow-xl"
        >
          {localeOptions.map((opt) => {
            const active = opt.locale === locale;
            return (
              <button
                key={opt.locale}
                type="button"
                role="menuitem"
                onClick={() => {
                  setLocale(opt.locale);
                  setOpen(false);
                }}
                className={`flex w-full items-center gap-2 px-3 py-2 text-left text-xs hover:bg-slate-800 ${
                  active ? 'bg-slate-800 text-sky-300' : 'text-slate-200'
                }`}
              >
                <span className="text-base leading-none">{opt.flag}</span>
                <span>{opt.label}</span>
              </button>
            );
          })}
        </div>
      )}
    </div>
  );
}
